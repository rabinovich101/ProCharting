// Generates app/emoji-data.ts from the Unicode emoji-test.txt grouped list.
// Mirrors TradingView's "Icon" tool emoji catalog: full Unicode set, grouped
// into 8 categories, skin-tone variants and the Component group dropped.
//
// Usage:
//   curl -s https://unicode.org/Public/emoji/15.1/emoji-test.txt -o /tmp/emoji-test.txt
//   node scripts/gen-emoji-data.mjs /tmp/emoji-test.txt
//
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const inputPath = process.argv[2] || '/tmp/emoji-test.txt';
const outputPath = join(__dirname, '..', 'app', 'emoji-data.ts');

// Unicode group name -> our TradingView-style category id.
const GROUP_TO_CATEGORY = {
  'Smileys & Emotion': 'smileys',
  'People & Body': 'smileys',
  'Animals & Nature': 'nature',
  'Food & Drink': 'food',
  Activities: 'activity',
  'Travel & Places': 'travel',
  Objects: 'objects',
  Symbols: 'symbols',
  Flags: 'flags',
};

// Display order + label + representative category-bar glyph (matches TV).
const CATEGORY_ORDER = [
  { id: 'smileys', label: 'Smileys & people', icon: '😀' },
  { id: 'nature', label: 'Animals & nature', icon: '🦄' },
  { id: 'food', label: 'Food & drink', icon: '🍔' },
  { id: 'activity', label: 'Activity', icon: '🏀' },
  { id: 'travel', label: 'Travel & places', icon: '🚀' },
  { id: 'objects', label: 'Objects', icon: '💡' },
  { id: 'symbols', label: 'Symbols', icon: '❤️' },
  { id: 'flags', label: 'Flags', icon: '🚩' },
];

const SKIN_TONE = /[\u{1F3FB}-\u{1F3FF}]/u;

const buckets = new Map(CATEGORY_ORDER.map((c) => [c.id, []]));
let currentGroup = null;

for (const raw of readFileSync(inputPath, 'utf8').split('\n')) {
  const line = raw.trimEnd();
  if (line.startsWith('# group:')) {
    currentGroup = line.slice('# group:'.length).trim();
    continue;
  }
  if (!line || line.startsWith('#')) continue;

  // <codepoints> ; <status> # <emoji> E<ver> <name>
  const [data, comment] = line.split('#');
  if (!comment) continue;
  const [codes, status] = data.split(';').map((s) => s.trim());
  if (status !== 'fully-qualified') continue;

  const category = GROUP_TO_CATEGORY[currentGroup];
  if (!category) continue; // drops the Component group

  const char = String.fromCodePoint(...codes.split(/\s+/).map((h) => parseInt(h, 16)));
  if (SKIN_TONE.test(char)) continue; // keep tone-neutral base only

  const name = comment.trim().replace(/^\S+\s+E[\d.]+\s+/, '');
  buckets.get(category).push({ c: char, n: name });
}

const categories = CATEGORY_ORDER.map((c) => ({ ...c, emojis: buckets.get(c.id) }));
const total = categories.reduce((sum, c) => sum + c.emojis.length, 0);

const body = `// AUTO-GENERATED by scripts/gen-emoji-data.mjs from Unicode emoji-test.txt (15.1).
// ${total} emoji across ${categories.length} categories. Do not edit by hand.

export interface EmojiEntry {
  /** Fully-qualified emoji character (used for the native-font fallback). */
  c: string;
  /** Human-readable name, used for the accessible label. */
  n: string;
}

export interface EmojiCategory {
  id: string;
  label: string;
  /** Representative glyph shown in the category navigation bar. */
  icon: string;
  emojis: EmojiEntry[];
}

export const EMOJI_CATEGORIES: EmojiCategory[] = ${JSON.stringify(categories)};

// jsDelivr-hosted Twemoji (jdecked maintained fork) — flat SVG emoji that render
// identically on every OS, matching TradingView's vector emoji set.
export const TWEMOJI_SVG_BASE = 'https://cdn.jsdelivr.net/gh/jdecked/twemoji@15.1.0/assets/svg/';

// Replicates twemoji's toCodePoint + variation-selector handling so filenames
// line up with the Twemoji asset set.
export const emojiToTwemojiCode = (emoji: string): string => {
  const cleaned = emoji.indexOf('\\u200d') < 0 ? emoji.replace(/\\uFE0F/g, '') : emoji;
  const points: string[] = [];
  let high = 0;
  for (let i = 0; i < cleaned.length; i += 1) {
    const code = cleaned.charCodeAt(i);
    if (high) {
      points.push((0x10000 + ((high - 0xd800) << 10) + (code - 0xdc00)).toString(16));
      high = 0;
    } else if (code >= 0xd800 && code <= 0xdbff) {
      high = code;
    } else {
      points.push(code.toString(16));
    }
  }
  return points.join('-');
};

export const twemojiSvgUrl = (emoji: string): string => \`\${TWEMOJI_SVG_BASE}\${emojiToTwemojiCode(emoji)}.svg\`;
`;

writeFileSync(outputPath, body);
console.log(`Wrote ${outputPath}`);
for (const c of categories) console.log(`  ${c.id.padEnd(10)} ${c.emojis.length}`);
console.log(`  total      ${total}`);
